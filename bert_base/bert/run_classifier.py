class MrpcProcessor(DataProcessor):
    """Processor for the MRPC data set (GLUE version)."""

    # 提取train文件，返回一个类的数组
    def get_train_examples(self, data_dir):
        # 提取训练文件，返回一个类的数组
        return self._create_examples(
            # 读取csv文件，返回一个list
            self._read_tsv(os.path.join(data_dir, "train.csv")), "train")
    
    # 提取dev文件，返回一个类的数组
    def get_dev_examples(self, data_dir):
        """See base class."""
        return self._create_examples(
            self._read_tsv(os.path.join(data_dir, "dev.csv")), "dev")

    # 提取test文件，返回一个类的数组
    def get_test_examples(self, data_dir):
        """See base class."""
        return self._create_examples(
            self._read_tsv(os.path.join(data_dir, "test.csv")), "test")

    def get_labels(self):
        """See base class."""
        return ["0", "1"]

    # 返回一个list = [<__main__.InputExample object at 0x7fcf51668c88>, ...] 每个类中会有四个参数
    # self.guid = （train， 0）   、    self.text_a = 0下标的句子a    、   self.text_b = 0下标的句子b    、   self.label = 标签0 or 1
    def _create_examples(self, lines, set_type):
        """Creates examples for the training and dev sets."""
        # lines 句子相似度匹配 [句子1，句子2，0or1是否相似]
        # set_type 是 训练集 还是 测试集 or 交叉验证集
        examples = []
        for (i, line) in enumerate(lines):
            # print(f"index = {i}   \nset_type = {set_type}   \nelement = {line}   \n==========")
            if i == 0: # 标注文件的抬头 ['sentence1', 'sentence2', 'label']   
                continue
            guid = "%s-%s" % (set_type, i)
            # 对句子含有其他编码进行处理
            text_a = tokenization.convert_to_unicode(line[0])
            text_b = tokenization.convert_to_unicode(line[1])
            if set_type == "test":
                label = "0"
            else:
                label = tokenization.convert_to_unicode(line[2])
            examples.append(
                InputExample(guid=guid, text_a=text_a, text_b=text_b, label=label)
                )
        # [<__main__.InputExample object at 0x7fcf51668c88>, ...]
        return examples


